    <h3 class="fade-in">Sobre o Projeto</h3><br>
    <p class="fade-in2">&nbsp;Este site foi desenvolvido como parte de um trabalho acadêmico com o objetivo de criar uma ferramenta prática e acessível para a geração de currículos. A proposta é facilitar a vida de pessoas que desejam montar um currículo de forma rápida e organizada, sem depender de editores de texto ou modelos complicados.

